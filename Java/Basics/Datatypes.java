

public class Datatypes{
    public static void main(String[] args){
        //Data types are used to define the type of data a variable can hold
        //Primitive Data Types
        //8 Primitive Data Types
        //byte, short, int, long, float, double, char, boolean

        //byte - 1 byte 256 values - Size 1 byte
        byte by = 100;
        System.out.println("Byte: " + by);

        //short - 2 bytes - Size 2 bytes
        short sh = 10000;
        System.out.println("Short: " + sh);

        //long - 8 bytes - Size 8 bytes
        long lg = 100000;
        System.out.println("Long: " + lg);

        //char - character data type - Size 2 bytes
        char ch = 'A';
        System.out.println("Character: " + ch);

        //boolean - true or false data type - Size 1 bit
        boolean bool = true;
        System.out.println("Boolean: " + bool);

        //int - Integer data type (Whole numbers) - Size 4 bytes
        int a = 10;
        System.out.println("Integer: " + a);

        //float - Floating point data type (Decimal numbers) - Size 4 bytes
        float b = 10.5f;
        System.out.println("Float: " + b);

        //double - Double precision floating point data type (Decimal numbers) - Size 8 bytes
        double c = 20.99;
        System.out.println("Double: " + c);
    }
}